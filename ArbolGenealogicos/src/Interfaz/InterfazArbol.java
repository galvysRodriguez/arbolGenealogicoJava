    public void mostrar() {
        Persona raiz = ArbolGenealogico.getRaiz();
        String padre = ArbolGenealogico.SubirNivelPadre(raiz, 1).getNombre();
        etiquetaPadre.setText(padre);
        String madre = ArbolGenealogico.SubirNivelMadre(raiz, 1).getNombre();
        etiquetaMadre.setText(madre);
        String abueloP = ArbolGenealogico.SubirNivelPadre(raiz, 2).getNombre();
        etiquetaAbueloPaterno.setText(abueloP);
        String abuelaM = ArbolGenealogico.SubirNivelMadre(raiz, 2).getNombre();
        etiquetaAbuelaMaterno.setText(abuelaM);
        String abueloM = ArbolGenealogico.SubirNivelPadre(ArbolGenealogico.SubirNivelMadre(raiz, 1), 1).getNombre();
        etiquetaAbueloMaterno.setText(abueloM);
        String abuelaP = ArbolGenealogico.SubirNivelMadre(ArbolGenealogico.SubirNivelPadre(raiz, 1), 1).getNombre();
        etiquetaAbuelaPaterna.setText(abuelaP);
        etiquetaUsuario.setText(raiz.getNombre());
        String esposa = ArbolGenealogico.obtenerEsposa();
        etiquetaEsposa.setText(esposa);
    }
    
    public void verificarRelacion(Persona persona1, Persona persona2, String relacion){
        if(relacion.equals("Hij@")){
            if(persona1.getPadre() == persona2 && !persona2.getPadre().getNombre().equals("")){
                resultadoEtiqueta.setText("Si lo es");
            }
            else{
                resultadoEtiqueta.setText("No lo es");
            }
        }
        else if(relacion.equals("Padre")){
            if(persona1 == persona2.getPadre()){
                resultadoEtiqueta.setText("Si lo es");
            }
            else{
                resultadoEtiqueta.setText("No lo es");
            }
        }
        else if(relacion.equals("Herman@")){
            if(persona1.getPadre() == persona2.getPadre()){
                resultadoEtiqueta.setText("Si lo es");
            }
            else{
                resultadoEtiqueta.setText("No lo es");
            }
        }
    }

